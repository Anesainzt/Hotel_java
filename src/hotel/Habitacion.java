package hotel;

public class Habitacion {
	
	protected static int cantHabTot;
	protected static boolean ocupado = false;

	public static boolean isEmpty() {
		if (ocupado = true) {
			return false;
		}else {
			return true;
			
		}
	}
	
	public static int getCantHabTot() {
		return cantHabTot;
	}

}
